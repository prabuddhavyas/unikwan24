            <h3 class="sub_heading_24 fw_500 color_light_black blog_title">
              Here's our UX audit process: we research, evaluate, and execute:
            </h3>
            <h3 class="sub_heading_24 fw_500 color_light_black blog_title">
              1. Phase 1: Research
            </h3>
            <ul class="blog_post_content_lists">
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">Briefing:</span>
                We define the goals and expectations of the UX audit, including
                the budget and timeline.
              </li>
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span
                  class="fw_700">Benchmarking:</span> We identify the target audience and their personas to see if
                they match the current users. We also conduct competitor analysis to extract valuable information
                about the target audience.
              </li>
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">User
                  research:</span> We conduct user interviews and surveys to gather feedback from real users. This
                helps us to understand their needs and pain points, as well as their overall experience with the
                product or service.
              </li>
            </ul>
            <h3 class="sub_heading_24 fw_500 color_light_black blog_title">
              2. Phase 2: Evaluation
            </h3>
            <ul class="blog_post_content_lists">
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">Heuristic
                  evaluation:</span> We use a team of designers to assess the product or service for usability
                problems. We use Nielsen's usability heuristics as a guide.</li>
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">Competitive
                  analysis:</span> We compare the product or service to its competitors to identify strengths and
                weaknesses. This helps us to understand how the product or service can be improved.</li>
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">Sitemap and
                  wireframes:</span> We create a sitemap and wireframes to visualize the structure and layout of the
                product or service. This helps us to identify any potential problems with the user flow.</li>
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">Final
                  screens:</span> We create the final screens of the product or service, including all of the
                features and states. We also create a mobile version of the product or service.</li>
            </ul>
            <h3 class="sub_heading_24 fw_500 color_light_black blog_title">
              3. Phase 3: Execution
            </h3>
            <ul class="blog_post_content_lists">
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">Style guide:
                </span>We create a style guide that documents the design principles and guidelines for the product
                or service. This helps to ensure that the product or service has a consistent look and feel.</li>
              <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black"><span class="fw_700">GUI (Graphic
                  User Interface) design: </span> We design the graphical user interface (GUI) of the product or
                service. This includes the layout, colors, fonts, and icons.</li>
            </ul>
