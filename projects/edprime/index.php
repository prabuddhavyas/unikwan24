  const MAIN_PAGE_LOTTIE_PATH = "https://lottie.host/30fc1d4d-e111-4e03-8663-a60443977dd1/rd8zATGCv7.json";

  let mainPageAnimation = lottie.loadAnimation({
    container: document.getElementById("mainPageLottie"),
    path: MAIN_PAGE_LOTTIE_PATH,
    autoplay: false,
    loop: false,
    renderer: "svg",
  });

  var academicsAnimation;
  var comprehensiveAnimation;
  var onboardingHorizontalScrollContainer;
  let onboardingContainerWidth;
  var onboardingScrollWidth;
  var sliderContainer;
  var sliderContainerWidth;
  var sliderScrollWidth;

  mainPageAnimation.addEventListener("DOMLoaded", () => {
    const ACADEMICS_LOTTIE_PATH = "https://lottie.host/bb85f96b-7760-417c-8203-3c23d15e5daa/Ar18STqydM.json";

    academicsAnimation = lottie.loadAnimation({
      container: document.getElementById("academicsLottie"),
      path: ACADEMICS_LOTTIE_PATH,
      autoplay: false,
      loop: false,
      renderer: "svg",
    });

    academicsAnimation.addEventListener("DOMLoaded", () => {
      const COMPREHENSIVE_LOTTIE_PATH = "https://lottie.host/d0e5a91d-940c-4cc5-b7a2-420fb7031270/wygOyIB68E.json";

      comprehensiveAnimation = lottie.loadAnimation({
        container: document.getElementById("comprehensiveLottie"),
        path: COMPREHENSIVE_LOTTIE_PATH,
        autoplay: false,
        loop: false,
        renderer: "svg",
      });

      comprehensiveAnimation.addEventListener("DOMLoaded", () => {
        gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

        onboardingHorizontalScrollContainer = document.querySelector(".onboarding_img_container");
        onboardingContainerWidth = onboardingHorizontalScrollContainer.scrollWidth;
        onboardingScrollWidth = onboardingContainerWidth - window.innerWidth;

        gsap.to(".onboarding_img_container", {
          x: () => -onboardingScrollWidth,
          ease: "none",
          scrollTrigger: {
            invalidateOnRefresh: true,
            trigger: ".onboarding_img_container",
            pin: true,
            scrub: 1,
            start: "top top",
            end: () => {
              `+=${onboardingContainerWidth}`
            }
          }
        });

        sliderContainer = document.querySelector('.showcase_image_container');
        sliderContainerWidth = sliderContainer.scrollWidth;
        sliderScrollWidth = sliderContainerWidth - window.innerWidth;

        let sliderAnimation = gsap.to(".showcase_image_container", {
          x: () => -sliderScrollWidth,
          duration: 10,
          ease: "none",
          paused: true,
          scrollTrigger: {
            trigger: ".showcase_section",
            start: "top bottom",
            end: "bottom top",
            toggleActions: "resume pause resume pause",
            invalidateOnRefresh: true,
          },
          onComplete: () => {
            setTimeout(() => {
              sliderAnimation.reverse();
            }, 5000);
          },
          onReverseComplete: () => {
            sliderAnimation.restart();
          }
        });

        $(window).scroll(function () {
          var window_top_position = $(window).scrollTop();
          var window_bottom_position = (window_top_position + $(window).height());

          var main_self_section = $(".main_page_lottie_container").offset().top;
          var main_bottom_section = $(".academics_page_section").offset().top;
          var main_element_height = $('.main_page_section').height();
          var main_element_top_position = $('.main_page_section').offset().top + 500;
          var main_scrolldown_element_bottom_position = (main_element_top_position +
            main_element_height) - 800;
          var main_scrollup_element_bottom_position = (main_element_top_position +
            main_element_height) - 800;

          var academics_self_section = $(".academics_lottie_container").offset().top;
          var academics_bottom_section = $(".fee_management_section").offset().top;
          var academics_element_height = $('.academics_page_section').height();
          var academics_element_top_position = $('.academics_page_section').offset().top + 500;
