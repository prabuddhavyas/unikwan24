<script>
  const JEEVN_ONBOARDING_LOTTIE_PATH = "https://lottie.host/d38ced65-d657-4a1e-8947-c3a3b47d5cc3/5FfHCAX0vl.json";

  let jeevnOnboardingAnimation = lottie.loadAnimation({
    container: document.getElementById("jeevnOnboardingLottie"),
    path: JEEVN_ONBOARDING_LOTTIE_PATH,
    autoplay: false,
    loop: false,
    renderer: "svg",
  });

  var healthInsightsAnimation;
  var realTimeAnimation;
  var sliderContainer;
  var sliderContainerWidth;
  var sliderScrollWidth;

  jeevnOnboardingAnimation.addEventListener("DOMLoaded", () => {
    const HEALTH_INSIGHTS_LOTTIE_PATH = "https://lottie.host/81452329-a96e-4bb0-8da5-9f59df98f8ee/6d4cJ6FKwb.json";

    healthInsightsAnimation = lottie.loadAnimation({
      container: document.getElementById("healthInsightsLottie"),
      path: HEALTH_INSIGHTS_LOTTIE_PATH,
      autoplay: false,
      loop: false,
      renderer: "svg",
    });

    healthInsightsAnimation.addEventListener("DOMLoaded", () => {
      const REAL_TIME_LOTTIE_PATH = "https://lottie.host/bfae43b8-ecc1-4d4f-9643-df3068272f37/4nFd8CpNnQ.json";

      realTimeAnimation = lottie.loadAnimation({
        container: document.getElementById("realTimeLottie"),
        path: REAL_TIME_LOTTIE_PATH,
        autoplay: false,
        loop: false,
        renderer: "svg",
      });

      realTimeAnimation.addEventListener("DOMLoaded", () => {
        gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

        sliderContainer = document.querySelector('.showcase_image_container');
        sliderContainerWidth = sliderContainer.scrollWidth;
        sliderScrollWidth = sliderContainerWidth - window.innerWidth;

        let sliderAnimation = gsap.to(".showcase_image_container", {
          x: () => -sliderScrollWidth,
          duration: 10,
          ease: "none",
          paused: true,
          scrollTrigger: {
            trigger: ".showcase_section",
            start: "top bottom",
            end: "bottom top",
            toggleActions: "resume pause resume pause",
            invalidateOnRefresh: true,
          },
          onComplete: () => {
            setTimeout(() => {
              sliderAnimation.reverse();
            }, 5000);
          },
          onReverseComplete: () => {
            sliderAnimation.restart();
          }
        });

        $(window).scroll(function () {
          var window_top_position = $(window).scrollTop();
          var window_bottom_position = (window_top_position + $(window).height());

          var jeevn_onboarding_self_section = $(".jeevn_onboarding_lottie_container").offset().top;
          var jeevn_onboarding_bottom_section = $(".post_onboarding_section").offset().top;
          var jeevn_onboarding_element_height = $('.jeevn_onboarding_section').height();
          var jeevn_onboarding_element_top_position = $('.jeevn_onboarding_section').offset().top + 500;
          var jeevn_onboarding_scrolldown_element_bottom_position = (jeevn_onboarding_element_top_position +
            jeevn_onboarding_element_height) - 800;
          var jeevn_onboarding_scrollup_element_bottom_position = (jeevn_onboarding_element_top_position +
            jeevn_onboarding_element_height) - 800;

          var health_insights_self_section = $(".health_insights_lottie_container").offset().top;
          var health_insights_bottom_section = $(".journey_view_section").offset().top;
          var health_insights_element_height = $('.health_insights_section').height();
          var health_insights_element_top_position = $('.health_insights_section').offset().top + 500;
          var health_insights_scrolldown_element_bottom_position = (health_insights_element_top_position +
            health_insights_element_height) - 800;
          var health_insights_scrollup_element_bottom_position = (health_insights_element_top_position +
            health_insights_element_height) - 800;

          var real_time_self_section = $(".real_time_lottie_container").offset().top;
          var real_time_bottom_section = $(".showcase_section").offset().top;
          var real_time_element_height = $('.dashboard_design_section').height();
          var real_time_element_top_position = $('.dashboard_design_section').offset().top + 500;
          var real_time_scrolldown_element_bottom_position = (real_time_element_top_position +
            real_time_element_height) - 800;
          var real_time_scrollup_element_bottom_position = (real_time_element_top_position +
            real_time_element_height) - 800;

          if ((window_bottom_position >= jeevn_onboarding_element_top_position) && (window_top_position <=
            jeevn_onboarding_scrolldown_element_bottom_position)) {
            jeevnOnboardingAnimation.play();
          } else if ((window_bottom_position >= jeevn_onboarding_element_top_position) && (window_top_position <=
            jeevn_onboarding_scrollup_element_bottom_position)) {
            jeevnOnboardingAnimation.play();
          } else if (window_top_position > jeevn_onboarding_bottom_section) {
            jeevnOnboardingAnimation.stop();
          } else if (window_bottom_position < jeevn_onboarding_self_section) {
            jeevnOnboardingAnimation.stop();
          }

          if ((window_bottom_position >= health_insights_element_top_position) && (window_top_position <=
            health_insights_scrolldown_element_bottom_position)) {
            healthInsightsAnimation.play();
          } else if ((window_bottom_position >= health_insights_element_top_position) && (window_top_position <=
            health_insights_scrollup_element_bottom_position)) {
            healthInsightsAnimation.play();
          } else if (window_top_position > health_insights_bottom_section) {
            healthInsightsAnimation.stop();
          } else if (window_bottom_position < health_insights_self_section) {
            healthInsightsAnimation.stop();
          }

          if ((window_bottom_position >= real_time_element_top_position) && (window_top_position <=
            real_time_scrolldown_element_bottom_position)) {
            realTimeAnimation.play();
          } else if ((window_bottom_position >= real_time_element_top_position) && (window_top_position <=
            real_time_scrollup_element_bottom_position)) {
            realTimeAnimation.play();
          } else if (window_top_position > real_time_bottom_section) {
            realTimeAnimation.stop();
          } else if (window_bottom_position < real_time_self_section) {
            realTimeAnimation.stop();
          }
        });
      });
    });
  });

  $(window).on('resize orientationchange', function () {
    sliderContainer = document.querySelector('.showcase_image_container');
    sliderContainerWidth = sliderContainer.scrollWidth;
    sliderScrollWidth = sliderContainerWidth - $(window).innerWidth();

    ScrollTrigger.refresh();
  });

  function otherProjectsHover(event) {
    if ($(window).width() < 1155) {
      $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      event.preventDefault();
    }
    else {
      var hoverId = event.target.id;
      if (event.type == "mouseenter") {
        if (hoverId === "hover_left") {
          $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_right");
          setTimeout(() => {
            $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
        else {
          $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_left");
          setTimeout(() => {
            $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  function handleOtherProjectsResize(event) {
    var windowWidth = $(window).width();
    if (event.type === "resize" || event.type === "change") {
      if (windowWidth < 1155) {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  window.addEventListener("resize", handleOtherProjectsResize);
  screen.orientation.addEventListener("change", handleOtherProjectsResize);
</script>

