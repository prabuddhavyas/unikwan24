    function initLottieScrollControl() {
      $(window).scroll(function() {
        const winTop = $(window).scrollTop();
        const winBottom = winTop + $(window).height();

        // ── Player lottie: welcomeVisist ─────────────────────────
        if (activePortal === 'player') {
          triggerLottie(
            welcomeVisistAnimation,
            '.welcome_visist_section',
            '.player_profile_section',
            winTop, winBottom
          );

          triggerLottie(
            chooseGameAnimation,
            '#portal_player_content .seamless_section',
            '#portal_player_content .visist_design_section',
            winTop,
            winBottom
          );

          triggerLottie(
            playerAnimation,
            '.visist_design_section',
            '.visist_showcase_section',
            winTop, winBottom
          );
        }

        // ── Coach lottie ─────────────────────────────────────────
        if (activePortal === 'coach') {
          triggerLottie(
            coachWelcomeAnimation,
            '#portal_coach_content .player_profile_section',
            '#portal_coach_content .visist_horizontal_scroll',
            winTop,
            winBottom
          );

          triggerLottie(
            coachLottieAnimation,
            '#portal_coach_content .seamless_section',
            '#portal_coach_content .visist_design_section',
            winTop,
            winBottom
          );
        }
      });
    }

    function triggerLottie(animation, selfSel, stopSel, winTop, winBottom) {
      if (!animation) return;
      const selfEl = $(selfSel);
      const stopEl = $(stopSel);
      if (!selfEl.length || !stopEl.length) return;

      const elementTop = selfEl.offset().top + 500;
      const elementBottom = elementTop + selfEl.height() - 800;
      const stopPosition = stopEl.offset().top - 100;
      const selfTop = selfEl.offset().top;

      if ((winBottom >= elementTop) && (winTop <= elementBottom)) {
        animation.play();
      } else if (winTop > stopPosition) {
        animation.stop();
      } else if (winBottom < selfTop) {
        animation.stop();
      }
    }

