    // ── Horizontal scroll helper ─────────────────────────────────
    function createHorizontalScroll(containerSel) {
      const el = document.querySelector(containerSel);
      if (!el) return null;
      return gsap.to(containerSel, {
        x: () => -(el.scrollWidth - window.innerWidth),
        ease: "none",
        // scrollTrigger: {
        //   invalidateOnRefresh: true,
        //   trigger: containerSel,
        //   pin: true,
        //   scrub: 1,
        //   start: "top top",
        //   end: () => `+=${el.scrollWidth}`,
        // }
        scrollTrigger: {
          trigger: containerSel,
          pin: true,
          scrub: 1.2,
          anticipatePin: 1,
          invalidateOnRefresh: true,
          start: "top top",
          end: () => `+=${el.scrollWidth}`,
        }
      });
    }

    // ── Showcase slider helper ────────────────────────────────────
    function createShowcaseSlider(containerSel, triggerSel) {
      const sliderEl = document.querySelector(containerSel);
      if (!sliderEl) return null;
      const anim = gsap.to(containerSel, {
        // x: () => -(sliderEl.scrollWidth - window.innerWidth),
        x: () => {
          return -(sliderEl.scrollWidth - window.innerWidth);
        },
        force3D: true,
        duration: 10,
        ease: "none",
        paused: true,
        scrollTrigger: {
          trigger: triggerSel,
          start: "top bottom",
          end: "bottom top",
          toggleActions: "resume pause resume pause",
          invalidateOnRefresh: true,
        },
        onComplete: () => {
          setTimeout(() => anim.reverse(), 5000);
        },
        onReverseComplete: () => {
          anim.restart();
        }
      });
      return anim;
    }

