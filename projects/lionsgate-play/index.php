<script>
  const OTT_LOTTIE_PATH = "https://lottie.host/8aefe8c4-8f73-48ef-8b03-b5284fb5e1c8/vC5WO4iD6d.json";

  let ottAnimation = lottie.loadAnimation({
    container: document.getElementById("ottLottie"),
    path: OTT_LOTTIE_PATH,
    autoplay: false,
    loop: false,
    renderer: "svg",
  });

  var seamlessAnimation;
  var designAnimation;
  var personalisationHorizontalScrollContainer;
  let personalisationContainerWidth;
  var personalisationScrollWidth;
  var sliderContainer;
  var sliderContainerWidth;
  var sliderScrollWidth;

  ottAnimation.addEventListener("DOMLoaded", () => {
    const SEAMLESS_LOTTIE_PATH = "https://lottie.host/2ae1b955-88d5-4bb0-a432-6215c38f7b23/T6IPjIxYcD.json";

    let seamlessAnimation = lottie.loadAnimation({
      container: document.getElementById("seamlessLottie"),
      path: SEAMLESS_LOTTIE_PATH,
      autoplay: false,
      loop: false,
      renderer: "svg",
    });

    seamlessAnimation.addEventListener("DOMLoaded", () => {
      const DESIGN_LOTTIE_PATH = "https://lottie.host/4359880f-200b-41bb-a63f-6e4c0e6ab7e8/WjXLORKd0w.json";

      let designAnimation = lottie.loadAnimation({
        container: document.getElementById("designLottie"),
        path: DESIGN_LOTTIE_PATH,
        autoplay: false,
        loop: false,
        renderer: "svg",
      });

      designAnimation.addEventListener("DOMLoaded", () => {
        gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

        personalisationHorizontalScrollContainer = document.querySelector(".personalisation_img_container");
        personalisationContainerWidth = personalisationHorizontalScrollContainer.scrollWidth;
        personalisationScrollWidth = personalisationContainerWidth - window.innerWidth;

        gsap.to(".personalisation_img_container", {
          x: () => -personalisationScrollWidth,
          ease: "none",
          scrollTrigger: {
            invalidateOnRefresh: true,
            trigger: ".personalisation_img_container",
            pin: true,
            scrub: 1,
            start: "top top",
            end: () => {
              `+=${personalisationContainerWidth}`
            }
          }
        });

        sliderContainer = document.querySelector('.showcase_image_container');
        sliderContainerWidth = sliderContainer.scrollWidth;
        sliderScrollWidth = sliderContainerWidth - window.innerWidth;

        let sliderAnimation = gsap.to(".showcase_image_container", {
          x: () => -sliderScrollWidth,
          duration: 10,
          ease: "none",
          paused: true,
          scrollTrigger: {
            trigger: ".showcase_section",
            start: "top bottom",
            end: "bottom top",
            toggleActions: "resume pause resume pause",
            invalidateOnRefresh: true,
          },
          onComplete: () => {
            setTimeout(() => {
              sliderAnimation.reverse();
            }, 5000);
          },
          onReverseComplete: () => {
            sliderAnimation.restart();
          }
        });

        $(window).scroll(function () {
          var window_top_position = $(window).scrollTop();
          var window_bottom_position = (window_top_position + $(window).height());

          var ott_self_section = $(".ott_lottie_container").offset().top;
          var ott_bottom_section = $(".personalisation_section").offset().top - 100;
          var ott_element_height = $('.ott_section').height();
          var ott_element_top_position = $('.ott_section').offset().top + 500;
          var ott_scrolldown_element_bottom_position = (ott_element_top_position + ott_element_height) - 800;
          var ott_scrollup_element_bottom_position = (ott_element_top_position + ott_element_height) - 800;

          var seamless_self_section = $(".seamless_lottie_container").offset().top;
          var seamless_bottom_section = $(".content_preview_section").offset().top;
          var seamless_element_height = $('.seamless_section').height();
          var seamless_element_top_position = $('.seamless_section').offset().top + 500;
          var seamless_scrolldown_element_bottom_position = (seamless_element_top_position + seamless_element_height) - 800;
          var seamless_scrollup_element_bottom_position = (seamless_element_top_position + seamless_element_height) - 800;

          var design_self_section = $(".design_lottie_container").offset().top;
          var design_bottom_section = $(".multi_device_section").offset().top;
          var design_element_height = $('.lionsgate_design_section').height();
          var design_element_top_position = $('.lionsgate_design_section').offset().top + 500;
          var design_scrolldown_element_bottom_position = (design_element_top_position + design_element_height) - 800;
          var design_scrollup_element_bottom_position = (design_element_top_position + design_element_height) - 800;

          if ((window_bottom_position >= ott_element_top_position) && (window_top_position <= ott_scrolldown_element_bottom_position)) {
            ottAnimation.play();
          }
          else if ((window_bottom_position >= ott_element_top_position) && (window_top_position <= ott_scrollup_element_bottom_position)) {
            ottAnimation.play();
          }
          else if (window_top_position > ott_bottom_section) {
            ottAnimation.stop();
          }
          else if (window_bottom_position < ott_self_section) {
            ottAnimation.stop();
          }

          if ((window_bottom_position >= seamless_element_top_position) && (window_top_position <= seamless_scrolldown_element_bottom_position)) {
            seamlessAnimation.play();
          }
          else if ((window_bottom_position >= seamless_element_top_position) && (window_top_position <= seamless_scrollup_element_bottom_position)) {
            seamlessAnimation.play();
          }
          else if (window_top_position > seamless_bottom_section) {
            seamlessAnimation.stop();
          }
          else if (window_bottom_position < seamless_self_section) {
            seamlessAnimation.stop();
          }

          if ((window_bottom_position >= design_element_top_position) && (window_top_position <= design_scrolldown_element_bottom_position)) {
            designAnimation.play();
          }
          else if ((window_bottom_position >= design_element_top_position) && (window_top_position <= design_scrollup_element_bottom_position)) {
            designAnimation.play();
          }
          else if (window_top_position > design_bottom_section) {
            designAnimation.stop();
          }
          else if (window_bottom_position < design_self_section) {
            designAnimation.stop();
          }
        });
      });
    });
  });

  $(window).on('resize orientationchange', function () {
