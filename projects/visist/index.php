    const COACH_WELCOME_LOTTIE_PATH = "https://unikwan-website.s3.eu-north-1.amazonaws.com/Visist-coach-1.json";

    const COACH_LOTTIE_PATH = "https://unikwan-website.s3.eu-north-1.amazonaws.com/visist-coach-2.json";


    // ─── State ───────────────────────────────────────────────────
    let activePortal = 'player'; // 'player' | 'coach'

    // ─── Animation instances ─────────────────────────────────────
    let welcomeVisistAnimation = null;
    let playerAnimation = null;
    let chooseGameAnimation = null;
    let coachWelcomeAnimation = null;
    let coachLottieAnimation = null;

    // ─── GSAP ScrollTrigger instances ────────────────────────────
    let playerHorizontalST = null;
    let coachHorizontalST = null;
    let playerSliderAnim = null;
    let coachSliderAnim = null;

    // Track whether coach GSAP has been initialised at least once
    let coachGSAPInited = false;

    // ─────────────────────────────────────────────────────────────
    // TOGGLE LOGIC
    // ─────────────────────────────────────────────────────────────
    const toggleTrack = document.getElementById('portalToggle');
    const tabPlayer = document.getElementById('tab_player');
    const tabCoach = document.getElementById('tab_coach');
    const dynamicWrap = document.getElementById('portal_dynamic_sections');
    const playerContent = document.getElementById('portal_player_content');
    const coachContent = document.getElementById('portal_coach_content');

    function killST(anim) {
      if (!anim) return;
      if (anim.scrollTrigger) anim.scrollTrigger.kill();
      anim.kill();
    }

    function switchPortal(target) {
      if (target === activePortal) return;

      dynamicWrap.classList.add('switching');

      setTimeout(() => {
        activePortal = target;

        if (target === 'player') {
          toggleTrack.classList.remove('coach_active');
          tabPlayer.setAttribute('aria-selected', 'true');
          tabCoach.setAttribute('aria-selected', 'false');
          playerContent.classList.add('active');
          coachContent.classList.remove('active');

          if (coachWelcomeAnimation) coachWelcomeAnimation.stop();
          if (coachLottieAnimation) coachLottieAnimation.stop();

          // Kill coach ScrollTriggers — they must not fire while hidden
          killST(coachHorizontalST);
          coachHorizontalST = null;
          killST(coachSliderAnim);
          coachSliderAnim = null;
          coachGSAPInited = false;

          // Reinit player scroll animations (panel is visible again)
          requestAnimationFrame(() => {
            requestAnimationFrame(() => {
              initPlayerGSAP();

              setTimeout(() => {
                ScrollTrigger.refresh(true);
              }, 300);
            });
          });

        } else {
          toggleTrack.classList.add('coach_active');
          tabPlayer.setAttribute('aria-selected', 'false');
          tabCoach.setAttribute('aria-selected', 'true');
          coachContent.classList.add('active');
          playerContent.classList.remove('active');

          if (welcomeVisistAnimation) welcomeVisistAnimation.stop();
          if (chooseGameAnimation) chooseGameAnimation.stop();
          if (playerAnimation) playerAnimation.stop();

          // Kill player ScrollTriggers while hidden
          killST(playerHorizontalST);
          playerHorizontalST = null;
          killST(playerSliderAnim);
          playerSliderAnim = null;

          // Coach panel is now visible — init its GSAP.
          // Double rAF ensures the browser has painted the panel so
          // scrollWidth / offsetTop values are accurate.
          requestAnimationFrame(() => {
            requestAnimationFrame(() => {
              initCoachGSAP();

              setTimeout(() => {
                ScrollTrigger.refresh(true);
              }, 300);
            });
          });
        }

        dynamicWrap.classList.remove('switching');
        dynamicWrap.scrollIntoView({
          behavior: 'smooth',
          block: 'start'
        });
      }, 250);
    }

    // Click on track options
    tabPlayer.addEventListener('click', () => switchPortal('player'));
