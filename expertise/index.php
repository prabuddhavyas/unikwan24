    document.addEventListener("DOMContentLoaded", (event) => {
      gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

      const fadeItems = gsap.utils.toArray('.feature_inner_container');

      fadeItems.forEach((item, index) => {
        const prevItem = fadeItems[index - 1] || null;
        const nextItem = fadeItems[index + 1] || null;

        const tl = gsap.timeline({
          scrollTrigger: {
            trigger: item,
            start: 'top bottom',
            end: 'bottom top',
            scrub: 1,
            toggleActions: "play none reverse none",
          }
        });

        if(prevItem) {
          tl.to(prevItem, { opacity: 0, duration: 0.5, ease: "power2.out" }, 0);
        }

        if(index === 0) {
          tl.to(item, { opacity: 1, duration: 0.5, ease: "power2.in" });
        }

        if(nextItem) {
          tl.to(nextItem, { opacity: 1, duration: 0.5, ease: "power2.in" }, 0.5);
        }
      });

      const sections = gsap.utils.toArray(".feature_inner_container");
      sections.forEach((section, index) => {
        const min = parseFloat(section.dataset.min);
        const max = parseFloat(section.dataset.max);
        const progressFill = section.querySelector(".progress_fill");

        const progressTimeline = gsap.timeline({
          scrollTrigger: {
            trigger: section,
            start: "top bottom",
            end: "bottom top",
            scrub: 1,
            toggleActions: "play complete reverse complete",
          },
        });

        progressTimeline
          .fromTo(progressFill, { strokeDashoffset: min }, { strokeDashoffset: max, duration: 0.3, ease: "power3.inOut"})
          .to({}, { ease: "power3.inOut", duration: 0.1 });
      });

      let mediaQuery = gsap.matchMedia();
      mediaQuery.add(
        {
          isDesktop: "(min-width: 768px)",
          isMobile: "(max-width:  767.95px)",
        },
