            <h2 class="heading_48 fw_700 color_grey heading_width">
              What is a UX Audit?
            </h2>
            <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
              A user experience audit (UX audit) is a quality assurance (QA) process that identifies problematic aspects
              of a digital product. It demonstrates which features are frustrating for users.
            </p>
            <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
              In a UX design audit, the website's performance is carefully examined and assessed from the user's
              perspective. The audit attempts to identify any previously overlooked problems. Based on usability testing
              and user testing, a UX audit report reveals crucial areas for your site's improvement.
            </p>
          </div>
