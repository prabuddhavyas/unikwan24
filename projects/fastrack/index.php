<script src="https://cdn.jsdelivr.net/npm/gsap@3.12.5/dist/ScrollTrigger.min.js"></script>
<script src="https://cdn.jsdelivr.net/npm/gsap@3.12.5/dist/ScrollToPlugin.min.js"></script>

<script>
  const GAMIFIED_LOTTIE_PATH = "https://lottie.host/5e485a3c-eca4-44bd-85f3-f9246a09243f/RZDAmBTUOl.json";

  let gamifiedAnimation = lottie.loadAnimation({
    container: document.getElementById("gamifiedLottie"),
    path: GAMIFIED_LOTTIE_PATH,
    autoplay: false,
    loop: true,
    renderer: "svg",
  });

  var enhancedHorizontalScrollContainer;
  let enhancedContainerWidth;
  var enhancedScrollWidth;

  gamifiedAnimation.addEventListener("DOMLoaded", () => {
    gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

    enhancedHorizontalScrollContainer = document.querySelector(".enhanced_img_container");
    enhancedContainerWidth = enhancedHorizontalScrollContainer.scrollWidth;
    enhancedScrollWidth = enhancedContainerWidth - window.innerWidth;

    gsap.to(".enhanced_img_container", {
      x: () => -enhancedScrollWidth,
      ease: "none",
      scrollTrigger: {
        invalidateOnRefresh: true,
        trigger: ".enhanced_img_container",
        pin: true,
        scrub: 1,
        start: "top top",
        end: () => {
          `+=${enhancedContainerWidth}`
        }
      }
    });

    let mediaQuery = gsap.matchMedia();
    mediaQuery.add("(min-width: 768px)", () => {
      const fadeItems = gsap.utils.toArray('.brndimg');

      fadeItems.forEach((item, index) => {
        const prevItem = fadeItems[index - 1] || null;
        const nextItem = fadeItems[index + 1] || null;

        const tl = gsap.timeline({
          scrollTrigger: {
            trigger: item,
            start: 'top bottom',
            end: 'bottom top',
            scrub: 1,
            toggleActions: "play none reverse none"
          }
        });

        if (prevItem) {
          tl.to(prevItem, {
            opacity: 0,
            duration: 0.1,
            ease: "power2.out"
          }, 0);
        }

        if (index === 0 || index === 1) {
          tl.to(item, {
            opacity: index === 0 ? 1 : 0,
            duration: 0.1,
            ease: index === 0 ? "power2.in" : "power2.out"
          });
        }

        if (nextItem) {
          tl.to(nextItem, {
            opacity: 1,
            duration: 0.1,
            ease: "power2.in"
          }, 0.1);
        }
      });
    });

    mediaQuery.add(
      {
        isDesktop: "(min-width: 768px)",
        isMobile: "(max-width:  767.95px)",
      },
      (context) => {
        let { isDesktop, isMobile } = context.conditions;
        gsap.from(".brndimg1", {
          scrollTrigger: {
            trigger: ".brndimg1",
            scrub: 1,
            pin: true,
            pinSpacing: false,
            start: "top top",
            end: isDesktop ? "+=100%" : "+=200px"
          },
        });

        gsap.from(".brndimg2", {
          scrollTrigger: {
            trigger: ".brndimg2",
            scrub: 1,
            pin: true,
            pinSpacing: false,
            start: "top top",
            end: isDesktop ? "+=40px" : "+=40px"
          },
        });
      }
    );

    $(window).scroll(function () {
      var window_top_position = $(window).scrollTop();
      var window_bottom_position = (window_top_position + $(window).height());

      var gamified_self_section = $(".gamified_lottie_container").offset().top;
      var gamified_bottom_section = $(".building_brand_section").offset().top;
      var gamified_element_height = $('.gamified_section').height();
      var gamified_element_top_position = $('.gamified_section').offset().top + 500;
      var gamified_scrolldown_element_bottom_position = (gamified_element_top_position +
        gamified_element_height) - 800;
      var gamified_scrollup_element_bottom_position = (gamified_element_top_position + gamified_element_height) -
        800;

      if ((window_bottom_position >= gamified_element_top_position) && (window_top_position <=
        gamified_scrolldown_element_bottom_position)) {
        gamifiedAnimation.play();
      } else if ((window_bottom_position >= gamified_element_top_position) && (window_top_position <=
        gamified_scrollup_element_bottom_position)) {
        gamifiedAnimation.play();
      } else if (window_top_position > gamified_bottom_section) {
        gamifiedAnimation.stop();
      } else if (window_bottom_position < gamified_self_section) {
        gamifiedAnimation.stop();
      }
    });
  });

  $(window).on('resize orientationchange', function () {
    enhancedHorizontalScrollContainer = document.querySelector(".enhanced_img_container");
    enhancedContainerWidth = enhancedHorizontalScrollContainer.scrollWidth;
    enhancedScrollWidth = enhancedContainerWidth - $(window).innerWidth();
    ScrollTrigger.refresh();
  });

  function otherProjectsHover(event) {
    if ($(window).width() < 1155) {
      $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      event.preventDefault();
    }
    else {
      var hoverId = event.target.id;
      if (event.type == "mouseenter") {
        if (hoverId === "hover_left") {
          $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_right");
          setTimeout(() => {
            $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
        else {
          $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_left");
          setTimeout(() => {
            $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  function handleOtherProjectsResize(event) {
    var windowWidth = $(window).width();
    if (event.type === "resize" || event.type === "change") {
      if (windowWidth < 1155) {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  window.addEventListener("resize", handleOtherProjectsResize);
  screen.orientation.addEventListener("change", handleOtherProjectsResize);
</script>

