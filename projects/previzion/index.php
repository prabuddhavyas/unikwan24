<script>
  const DATA_VISUALIZATION_ACTION_LOTTIE_PATH = "https://unikwan-website.s3.eu-north-1.amazonaws.com/previzion-alert-monitoring.json";

  let dataVisualizationAnimation = lottie.loadAnimation({
    container: document.getElementById("dataVisualizationLottie"),
    path: DATA_VISUALIZATION_ACTION_LOTTIE_PATH,
    autoplay: false,
    loop: false,
    renderer: "svg",
  });

  var gamificationAnimation;
  var onboardingHorizontalScrollContainer;
  let onboardingContainerWidth;
  var onboardingScrollWidth;
  var sliderContainer;
  var sliderContainerWidth;
  var sliderScrollWidth;

  dataVisualizationAnimation.addEventListener("DOMLoaded", () => {
    const GAMIFICATION_LOTTIE_PATH = "https://unikwan-website.s3.eu-north-1.amazonaws.com/previzioin-camera-map.json";

    gamificationAnimation = lottie.loadAnimation({
      container: document.getElementById("gamificationLottie"),
      path: GAMIFICATION_LOTTIE_PATH,
      autoplay: false,
      loop: false,
      renderer: "svg",
    });

    gamificationAnimation.addEventListener("DOMLoaded", () => {
      gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

      onboardingHorizontalScrollContainer = document.querySelector(".navigation_interactions_img_container");
      onboardingContainerWidth = onboardingHorizontalScrollContainer.scrollWidth;
      onboardingScrollWidth = onboardingContainerWidth - window.innerWidth;

      gsap.to(".navigation_interactions_img_container", {
        x: () => -onboardingScrollWidth,
        ease: "none",
        force3D: true,
        scrollTrigger: {
          invalidateOnRefresh: true,
          trigger: ".navigation_interactions_img_container",
          pin: true,
          scrub: 1,
          start: "top top",
          end: () => {
            `+=${onboardingContainerWidth}`
          }
        }
      });

      sliderContainer = document.querySelector('.showcase_image_container');
      sliderContainerWidth = sliderContainer.scrollWidth;
      sliderScrollWidth = sliderContainerWidth - window.innerWidth;

      let sliderAnimation = gsap.to(".showcase_image_container", {
        x: () => -sliderScrollWidth,
        duration: 10,
        ease: "none",
        paused: true,
        scrollTrigger: {
          trigger: ".showcase_section",
          start: "top bottom",
          end: "bottom top",
          toggleActions: "resume pause resume pause",
          invalidateOnRefresh: true,
        },
        onComplete: () => {
          setTimeout(() => {
            sliderAnimation.reverse();
          }, 5000);
        },
        onReverseComplete: () => {
          sliderAnimation.restart();
        }
      });

      $(window).scroll(function () {
        var window_top_position = $(window).scrollTop();
        var window_bottom_position = (window_top_position + $(window).height());

        var data_visualization_self_section = $(".data_visualization_lottie_container").offset().top;
        var data_visualization_bottom_section = $(".visual_design_enhancement_section").offset().top;
        var data_visualization_element_height = $('.data_visualization_section').height();
        var data_visualization_element_top_position = $('.data_visualization_section').offset().top + 500;
        var data_visualization_scrolldown_element_bottom_position = (data_visualization_element_top_position +
          data_visualization_element_height) - 800;
        var data_visualization_scrollup_element_bottom_position = (data_visualization_element_top_position +
          data_visualization_element_height) - 800;

        var gamification_self_section = $(".gamification_lottie_container").offset().top;
        var gamification_bottom_section = $(".gamification_section").offset().top;
        var gamification_element_height = $('.visual_design_enhancement_section').height();
        var gamification_element_top_position = $('.visual_design_enhancement_section').offset().top + 500;
        var gamification_scrolldown_element_bottom_position = (gamification_element_top_position +
          gamification_element_height) - 800;
        var gamification_scrollup_element_bottom_position = (gamification_element_top_position +
          gamification_element_height) - 800;

        if ((window_bottom_position >= data_visualization_element_top_position) && (window_top_position <= data_visualization_scrolldown_element_bottom_position)) {
          dataVisualizationAnimation.play();
        } else if ((window_bottom_position >= data_visualization_element_top_position) && (window_top_position <= data_visualization_scrollup_element_bottom_position)) {
          dataVisualizationAnimation.play();
        } else if (window_top_position > data_visualization_bottom_section) {
          dataVisualizationAnimation.stop();
        } else if (window_bottom_position < data_visualization_self_section) {
          dataVisualizationAnimation.stop();
        }

        if ((window_bottom_position >= gamification_element_top_position) && (window_top_position <= gamification_scrolldown_element_bottom_position)) {
          gamificationAnimation.play();
        } else if ((window_bottom_position >= gamification_element_top_position) && (window_top_position <= gamification_scrollup_element_bottom_position)) {
          gamificationAnimation.play();
        } else if (window_top_position > gamification_bottom_section) {
          gamificationAnimation.stop();
        } else if (window_bottom_position < gamification_self_section) {
          gamificationAnimation.stop();
        }
      });
    });
  });

  $(window).on('resize orientationchange', function () {
    onboardingHorizontalScrollContainer = document.querySelector(".navigation_interactions_img_container");
    onboardingContainerWidth = onboardingHorizontalScrollContainer.scrollWidth;
    onboardingScrollWidth = onboardingContainerWidth - $(window).innerWidth();

    sliderContainer = document.querySelector('.showcase_image_container');
    sliderContainerWidth = sliderContainer.scrollWidth;
    sliderScrollWidth = sliderContainerWidth - $(window).innerWidth();

    ScrollTrigger.refresh();
  });

  function otherProjectsHover(event) {
    if ($(window).width() < 1155) {
      $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      event.preventDefault();
    }
    else {
      var hoverId = event.target.id;
      if (event.type == "mouseenter") {
        if (hoverId === "hover_left") {
          $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_right");
          setTimeout(() => {
            $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
        else {
          $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_left");
          setTimeout(() => {
            $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  function handleOtherProjectsResize(event) {
    var windowWidth = $(window).width();
    if (event.type === "resize" || event.type === "change") {
      if (windowWidth < 1155) {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  window.addEventListener("resize", handleOtherProjectsResize);
  screen.orientation.addEventListener("change", handleOtherProjectsResize);
</script>

