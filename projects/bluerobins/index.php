    // ─────────────────────────────────────────────────────────────
    // SHARED / GLOBAL GSAP: Conversion & Thread + Showcase Slider
    // ─────────────────────────────────────────────────────────────
    function createHorizontalScroll(containerSel) {
      const el = document.querySelector(containerSel);
      if (!el) return null;
      return gsap.to(containerSel, {
        x: () => -(el.scrollWidth - window.innerWidth),
        ease: "none",
        scrollTrigger: {
          trigger: containerSel,
          pin: true,
          scrub: 1.2,
          anticipatePin: 1,
          invalidateOnRefresh: true,
          start: "top top",
          end: () => `+=${el.scrollWidth}`,
        }
      });
    }

    function createShowcaseSlider(containerSel, triggerSel) {
      const sliderEl = document.querySelector(containerSel);
      if (!sliderEl) return null;
      const anim = gsap.to(containerSel, {
        x: () => -(sliderEl.scrollWidth - window.innerWidth),
        force3D: true,
        duration: 10,
        ease: "none",
        paused: true,
        scrollTrigger: {
          trigger: triggerSel,
          start: "top bottom",
          end: "bottom top",
          toggleActions: "resume pause resume pause",
          invalidateOnRefresh: true,
        },
        onComplete: () => {
          setTimeout(() => anim.reverse(), 5000);
        },
        onReverseComplete: () => {
          anim.restart();
        }
      });
      return anim;
    }

    function initGlobalGSAP() {
      gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

      killST(conversionThreadST);
      conversionThreadST = createHorizontalScroll("#conversion_thread_horizontal_scroll");

      killST(showcaseSliderAnim);
      showcaseSliderAnim = createShowcaseSlider("#player_showcase_container", ".bluerobins_showcase_section");

      ScrollTrigger.refresh();
