<script src="https://cdn.jsdelivr.net/npm/gsap@3.12.5/dist/ScrollToPlugin.min.js"></script>

<script>

  const STREAMLINE_LOTTIE_PATH = "https://lottie.host/b986846c-a2ef-4adc-ab68-5fc1762523a9/Bd3ufZ6e7u.json";

  let streamlineAnimation = lottie.loadAnimation({
    container: document.getElementById("streamlineLottie"),
    path: STREAMLINE_LOTTIE_PATH,
    autoplay: false,
    loop: true,
    renderer: "svg",
  });

  var voiceAnimation;
  var sliderContainer;
  var sliderContainerWidth;
  var sliderScrollWidth;

  streamlineAnimation.addEventListener("DOMLoaded", () => {
    const VOICE_LOTTIE_PATH = "https://lottie.host/9e462a4f-aef6-4b58-af02-2fc4b1e12971/N6zsvYorUg.json";

    voiceAnimation = lottie.loadAnimation({
      container: document.getElementById("voiceLottie"),
      path: VOICE_LOTTIE_PATH,
      autoplay: false,
      loop: true,
      renderer: "svg",
    });

    voiceAnimation.addEventListener("DOMLoaded", () => {
      gsap.registerPlugin(ScrollTrigger, ScrollToPlugin);

      sliderContainer = document.querySelector('.showcase_image_container');
      sliderContainerWidth = sliderContainer.scrollWidth;
      sliderScrollWidth = sliderContainerWidth - window.innerWidth;

      let sliderAnimation = gsap.to(".showcase_image_container", {
        x: () => -sliderScrollWidth,
        duration: 10,
        ease: "none",
        paused: true,
        scrollTrigger: {
          trigger: ".showcase_section",
          start: "top bottom",
          end: "bottom top",
          toggleActions: "resume pause resume pause",
          invalidateOnRefresh: true,
        },
        onComplete: () => {
          setTimeout(() => {
            sliderAnimation.reverse();
          }, 5000);
        },
        onReverseComplete: () => {
          sliderAnimation.restart();
        }
      });

      $(window).scroll(function() {
        var window_top_position = $(window).scrollTop();
        var window_bottom_position = (window_top_position + $(window).height());
        var streamline_self_section = $(".streamline_lottie_container").offset().top;

        var streamline_bottom_section = $(".task_management_section").offset().top - 100;
        var streamline_element_height = $('.streamline_service_section').height();
        var streamline_element_top_position = $('.streamline_service_section').offset().top + 500;
        var streamline_scrolldown_element_bottom_position = (streamline_element_top_position +
          streamline_element_height) - 800;
        var streamline_scrollup_element_bottom_position = (streamline_element_top_position + streamline_element_height) -
          800;

        var voice_self_section = $(".voice_lottie_container").offset().top;
        var voice_bottom_section = $(".design_system_section").offset().top;
        var voice_element_height = $('.voice_integration_section').height();
        var voice_element_top_position = $('.voice_integration_section').offset().top + 500;
        var voice_scrolldown_element_bottom_position = (voice_element_top_position + voice_element_height) - 800;
        var voice_scrollup_element_bottom_position = (voice_element_top_position + voice_element_height) - 800;

        if ((window_bottom_position >= streamline_element_top_position) && (window_top_position <=
            streamline_scrolldown_element_bottom_position)) {
          streamlineAnimation.play();
        } else if ((window_bottom_position >= streamline_element_top_position) && (window_top_position <=
            streamline_scrollup_element_bottom_position)) {
          streamlineAnimation.play();
        } else if (window_top_position > streamline_bottom_section) {
          streamlineAnimation.stop();
        } else if (window_bottom_position < streamline_self_section) {
          streamlineAnimation.stop();
        }

        if ((window_bottom_position >= voice_element_top_position) && (window_top_position <=
            voice_scrolldown_element_bottom_position)) {
          voiceAnimation.play();
        } else if ((window_bottom_position >= voice_element_top_position) && (window_top_position <=
            voice_scrollup_element_bottom_position)) {
          voiceAnimation.play();
        } else if (window_top_position > voice_bottom_section) {
          voiceAnimation.stop();
        } else if (window_bottom_position < voice_self_section) {
          voiceAnimation.stop();
        }
      });
    });
  });

  $(window).on('resize orientationchange', function() {
    sliderContainer = document.querySelector('.showcase_image_container');
    sliderContainerWidth = sliderContainer.scrollWidth;
    sliderScrollWidth = sliderContainerWidth - $(window).innerWidth();
    ScrollTrigger.refresh();
  });

  function otherProjectsHover(event) {
    if($(window).width() < 1155) {
      $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
      $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      event.preventDefault();
		}
    else {
      var hoverId = event.target.id;
      if(event.type == "mouseenter") {
        if(hoverId === "hover_left") {
          $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_right");
          setTimeout(() => {
            $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
        else {
          $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
          $("#" + hoverId).parent().stop().addClass("shrink_left");
          setTimeout(() => {
            $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'unset');
          }, 300);
        }
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  function handleOtherProjectsResize(event) {
    var windowWidth = $(window).width();
    if(event.type === "resize" || event.type === "change") {
      if(windowWidth < 1155) {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'auto');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
      else {
        $(".other_projects_right_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_left_container").find(".other_projects_button_container").css('pointer-events', 'none');
        $(".other_projects_hover_container").parent().stop().removeClass("shrink_left shrink_right");
      }
    }
  }

  window.addEventListener("resize", handleOtherProjectsResize);
  screen.orientation.addEventListener("change", handleOtherProjectsResize);
</script>

