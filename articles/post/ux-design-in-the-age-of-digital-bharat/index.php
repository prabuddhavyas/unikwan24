            <div id="scrollToSection2" class="margin_top_32">
              <h2 class="heading_48 fw_700 color_grey heading_width">
                Importance of UX design in the digital age
              </h2>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                Functionality (user interface) and the ability to enjoy (user experience) 
                a website are the synonyms for UX/UI. If the user does not get used to the UI/UX, 
                then their chances of becoming a buyer or someone who will be buying a product 
                from you can be critical.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                How can a bad UX/UI can be so important from the point of view of selling a product or services?
              </p>
              <p class="blog_post_mt fw_700 content_20 ff_satoshi color_black">
                Let us take a few real-life examples from the rural India:
              </p>
              <img class="post_dg_image_one blog_post_mt" src="<?php echo $baseUrl; ?>assets/images/rural-india.png" alt="rural india example" width="100%" height="100%" />
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                <span class="fw_700">1. Testament to the power of technology:</span> 
                With the 'JIO internet boom', consuming 1GB data in an hour is neither a 
                fantasy nor a sport for the elite. It is a reality and every part of India 
                is having a piece of it. The story is however a little different in rural India 
                because Internet might be cheap but a 3G smart-phone is distant dream.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                A country where the average daily wage lies is below 300 INR, procuring a smart-phone 
                can be a hard sport.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                This brings us to a real-life story of Arpita from a small village located 
                in Alipurduar, West Bengal. During the pandemic, Arpita realized the value 
                of internet connectivity and her father bought her a smart phone. Her school 
                education was entirely dependent on the smart-phone and its applications. 
                Initially she had a lot of difficulties using a few applications due to 
                which she had to resort to other applications.
              </p>
              <p class="blog_post_mt fw_700 content_20 ff_satoshi color_black">
                The main problems she faced were:
              </p>
              <ul class="blog_post_content_lists">
                <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black">The applications were not designed for slow internet connectivity.</li>
                <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black">The features were too complex for her to understand.</li>
                <li class="blog_post_mt fw_400 content_20 ff_satoshi color_black">The language used in the applications was not familiar to her.</li>
              </ul>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                This story helps us understand that there is an untouched market in rural India which is still waiting for a more user-friendly application. The story of Arpita is a clear-cut indication about the importance of User design.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                <span class="fw_700">2. Usability is not a luxury, it's a necessity:</span> 
                Anita is a 35-year-old woman from Mandawa, Rajasthan. She is a homemaker and has two young children. She recently started using a financial application to manage her finances. However, she is concerned about clicking the wrong button on the application. She is worried that she might accidentally make a payment or transfer money that she does not intend to.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                Anita is not alone in her concerns. Many people are hesitant to use financial applications because they are afraid of making mistakes. This is especially true for people who are not familiar with technology.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                UX/UI designers can help to address these concerns by making financial applications more user-friendly. This includes using clear and concise language, providing clear instructions, and making sure that the website is easy to navigate.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                By taking these steps, UX/UI designers can help to make financial applications more accessible and user-friendly. This will help to put people's minds at ease and make them more likely to use these applications.
              </p>
              <p class="blog_post_mt fw_700 content_20 ff_satoshi color_black">
                Now that you can have a faint idea about the after-effects of a bad UX design, let us discuss its importance.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                <span class="fw_700">1. It improves 'brand-image' </span> 
                A brand gets identified only when it offers something unique. This can be a "great Diwali sale" or an "end-of-season sale," but this comes in the secondary stage. In the primary stage, when a consumer starts to use an application for the first time, it's the user experience that will decide if he or she is going to come back and shop happily ever after!
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                <span class="fw_700">2. It catalyses product/service penetration </span> 
                In the secondary stage, after you have convinced your consumer to stay, comes the part where a viewer turns into a buyer. An interactive design, added with accessible icons, catalyses products and services into the minds of the consumer.
              </p>
              <p class="blog_post_mt fw_400 content_20 ff_satoshi color_black">
                <span class="fw_700">3. It converts 'searchers' into 'buyers' </span> 
                In the tertiary, but final stage, your consumer eventually turns into a buyer. It's no secret that a fancy restaurant brings curiosity in the mind of a new customer (primary and secondary stage). But it's their food and services (tertiary stage) that brings them back again and again.
              </p>
