 gsap.registerPlugin(ScrollTrigger);

let sliderAnimation;

function initShowcaseSlider() {
  const sliderContainer = document.querySelector(".showcase_image_container");

  if (!sliderContainer) return;

  const distance = sliderContainer.scrollWidth - window.innerWidth;

  // Don't initialize until width is available
  if (distance <= 0) {
    requestAnimationFrame(initShowcaseSlider);
    return;
  }

  if (sliderAnimation) {
    sliderAnimation.kill();
    ScrollTrigger.getAll().forEach(st => st.kill());
  }

  sliderAnimation = gsap.to(sliderContainer, {
    x: -distance,
    duration: 10,
    ease: "none",
    paused: true,
    scrollTrigger: {
      trigger: ".showcase_section",
      start: "top bottom",
      end: "bottom top",
      toggleActions: "resume pause resume pause",
      invalidateOnRefresh: true
    },
    onComplete() {
      setTimeout(() => sliderAnimation.reverse(), 5000);
    },
    onReverseComplete() {
      sliderAnimation.restart();
    }
  });

  ScrollTrigger.refresh();
}

window.addEventListener("load", () => {
  requestAnimationFrame(() => {
    requestAnimationFrame(initShowcaseSlider);
  });
});

window.addEventListener("resize", () => {
  clearTimeout(window.sliderResizeTimer);

  window.sliderResizeTimer = setTimeout(() => {
    initShowcaseSlider();
