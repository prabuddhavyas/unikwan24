    // ─────────────────────────────────────────────────────────────
    // PORTAL SWITCHING
    // ─────────────────────────────────────────────────────────────
    function switchPortal(target) {
      if (!PORTAL_ORDER.includes(target) || target === activePortal) return;

      dynamicWrap.classList.add('switching');

      setTimeout(() => {
        // Deactivate current portal's animations
        stopPortalLotties(activePortal);

        // Activate selected portal
        activePortal = target;
        updateTabs(target);
        updateContentBlocks(target);
        movePill(target);

        // Reinitialize (or lazily initialize) portal-specific animations
        initPortalLotties(target);

        dynamicWrap.classList.remove('switching');

        // Refresh ScrollTrigger after layout settles (double rAF ensures
        // the browser has painted the newly visible panel — and the page's
        // new total height, since portals have different section counts —
        // before we recompute scrollWidth / offsetTop dependent triggers)
        requestAnimationFrame(() => {
          requestAnimationFrame(() => {
            setTimeout(() => {
              ScrollTrigger.refresh(true);
            }, 300);
          });
        });

        dynamicWrap.scrollIntoView({
          behavior: 'smooth',
          block: 'start'
        });
      }, 250);
    }

    // ─────────────────────────────────────────────────────────────
    // TAB EVENT BINDING (click + full keyboard accessibility)
    // ─────────────────────────────────────────────────────────────
    PORTAL_ORDER.forEach(name => {
      const tab = TABS[name];

      tab.addEventListener('click', () => switchPortal(name));

      tab.addEventListener('keydown', (e) => {
        const currentIndex = PORTAL_ORDER.indexOf(name);

        if (e.key === 'Enter' || e.key === ' ') {
          e.preventDefault();
          switchPortal(name);
        } else if (e.key === 'ArrowRight' || e.key === 'ArrowDown') {
          e.preventDefault();
          const nextName = PORTAL_ORDER[(currentIndex + 1) % PORTAL_ORDER.length];
          TABS[nextName].focus();
          switchPortal(nextName);
        } else if (e.key === 'ArrowLeft' || e.key === 'ArrowUp') {
          e.preventDefault();
          const prevName = PORTAL_ORDER[(currentIndex - 1 + PORTAL_ORDER.length) % PORTAL_ORDER.length];
          TABS[prevName].focus();
          switchPortal(prevName);
        } else if (e.key === 'Home') {
          e.preventDefault();
          TABS[PORTAL_ORDER[0]].focus();
          switchPortal(PORTAL_ORDER[0]);
        } else if (e.key === 'End') {
          e.preventDefault();
          TABS[PORTAL_ORDER[PORTAL_ORDER.length - 1]].focus();
          switchPortal(PORTAL_ORDER[PORTAL_ORDER.length - 1]);
        }
      });
    });

    // ─────────────────────────────────────────────────────────────
    // SCROLL-DRIVEN LOTTIE PLAYBACK
    // Selectors below are matched to where each Lottie container
    // ACTUALLY sits in the markup (self = its own section, stop =
    // the next section that exists). ".bluerobins_showcase_section"
    // is used as the stop marker wherever a portal's Lottie sits in
    // the last section of that portal (Mentor Section 3, Parent
    // Section 2, Student Section 4), since it's the section that
    // always follows the portal content regardless of which portal
    // is active.
    // ─────────────────────────────────────────────────────────────
    function triggerLottie(animation, selfSel, stopSel, winTop, winBottom) {
      if (!animation) return;
      const selfEl = $(selfSel);
      const stopEl = $(stopSel);
      if (!selfEl.length || !stopEl.length) return;

      const elementTop = selfEl.offset().top + 500;
      const elementBottom = elementTop + selfEl.height() - 800;
      const stopPosition = stopEl.offset().top - 100;
      const selfTop = selfEl.offset().top;

      if ((winBottom >= elementTop) && (winTop <= elementBottom)) {
        animation.play();
      } else if (winTop > stopPosition) {
        animation.stop();
      } else if (winBottom < selfTop) {
        animation.stop();
      }
    }

    function initLottieScrollControl() {
      $(window).scroll(function() {
        const winTop = $(window).scrollTop();
        const winBottom = winTop + $(window).height();

        if (activePortal === 'student') {
          // Section 1 (portal_section) -> plays until Section 2 (player_profile_section)
          triggerLottie(
            studentWelcomeAnimation,
            '#portal_student_content .portal_section',
            '#portal_student_content .player_profile_section',
            winTop, winBottom
          );
          // studentLearningPathLottie lives in Section 4 (visist_design_section) —
          // self must match that, not Section 3 (seamless_section), which is
          // now just an image. Stops at the shared showcase section that follows.
          triggerLottie(
            studentLearningPathAnimation,
            '#portal_student_content .visist_design_section',
            '.bluerobins_showcase_section',
            winTop, winBottom
          );
        }

        if (activePortal === 'mentor') {
          // Section 1 (portal_section) -> plays until Section 2 (player_profile_section)
          triggerLottie(
            mentorDashboardAnimation,
            '#portal_mentor_content .portal_section',
            '#portal_mentor_content .player_profile_section',
            winTop, winBottom
          );
          // mentorSessionReviewLottie lives in Section 3 (seamless_section), which
          // is the LAST section of the Mentor portal — there is no
          // ".visist_design_section" here, so stop against the shared showcase
          // section that follows instead.
          triggerLottie(
            mentorSessionReviewAnimation,
            '#portal_mentor_content .earn_and_grow_section',
            '.bluerobins_showcase_section',
            winTop, winBottom
          );
        }

        if (activePortal === 'parent') {
          // Section 1 (portal_section) -> plays until Section 2 (player_profile_section)
          triggerLottie(
            parentOverviewAnimation,
            '#portal_parent_content .portal_section',
            '#portal_parent_content .player_profile_section',
            winTop, winBottom
          );
          // parentCommunicationLottie lives in Section 2 (player_profile_section),
          // not Section 3 (seamless_section) — self corrected to match, and it
          // stops at Section 3, the next section that actually exists.
          triggerLottie(
            parentCommunicationAnimation,
            '#portal_parent_content .player_profile_section',
            '#portal_parent_content .seamless_section',
            winTop, winBottom
          );
        }
      });
    }

