          var comprehensive_scrollup_element_bottom_position = (comprehensive_element_top_position +
            comprehensive_element_height) - 800;

          if ((window_bottom_position >= main_element_top_position) && (window_top_position <=
            main_scrolldown_element_bottom_position)) {
            mainPageAnimation.play();
          } else if ((window_bottom_position >= main_element_top_position) && (window_top_position <=
            main_scrollup_element_bottom_position)) {
            mainPageAnimation.play();
          } else if (window_top_position > main_bottom_section) {
            mainPageAnimation.stop();
          } else if (window_bottom_position < main_self_section) {
            mainPageAnimation.stop();
          }

          if ((window_bottom_position >= academics_element_top_position) && (window_top_position <=
            academics_scrolldown_element_bottom_position)) {
            academicsAnimation.play();
          } else if ((window_bottom_position >= academics_element_top_position) && (window_top_position <=
